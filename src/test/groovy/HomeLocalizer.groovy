/**
 * Indicates what is localized in HomePage.
 * 
 * @author Eduard Fugarolas
 *
 */
interface HomeLocalizer {

	/**
	 * The <code>action</code> attribute of the
	 * <code>form</code> element in the dictionary.
	 * 
	 * @return "action" String
	 */
	String getActionName()

	/**
	 * Main text of the dictionary header.
	 * 
	 * @return Main title.
	 */
	String getMainTitle()
}
